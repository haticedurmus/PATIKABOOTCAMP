package com.company;

public class Marka {

    int id;
    String name;

    public Marka(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public Marka(){}

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}

